# 14.3.1 정적 메소드
#===============================================================================

#------------------------------------------------------------------------------ 
# @staticmethod : 정적 메소드 장식자

class D:
	@staticmethod
	def add(x, y):
		return x+y
	
print(D.add(2, 3))
#5

d = D()
print(d.add(2, 3))
#5
#------------------------------------------------------------------------------

#------------------------------------------------------------------------------ 
# 클래스 멤버 참조

class E:
	acc = 0
	@staticmethod
	def accumulate(v):
		E.acc += v
		return E.acc
	
print(E.accumulate(10))
#10
print(E.accumulate(20))
#30
print(E.acc)
#30
#------------------------------------------------------------------------------ 

#===============================================================================
# 14.3.2 클래스 메소드
#===============================================================================

#------------------------------------------------------------------------------ 
# @classmethod : 클래스 메소드 장식자

class CM:
	acc = 0
	@classmethod
	def accumulate(cls, v):
		cls.acc += v
		return cls.acc

print(CM.accumulate(10))
#10
print(CM.accumulate(20))
#30
print(CM.acc)
#30

c = CM()
print(c.accumulate(5))
#35
print(CM.acc)
#35
print(c.__class__ is CM)
#True
